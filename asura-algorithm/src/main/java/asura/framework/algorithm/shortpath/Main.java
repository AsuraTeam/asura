	/**
	 * 
	 * 计算两个节点的直线距离
	 *
	 * @author zhangshaobin
	 * @created 2015年6月20日 下午5:40:38
	 *
	 * @param p1
	 * @param p2
	 * @return
	 */
	public static double shortPath(Point p1, Point p2) {
		return Math.sqrt(Math.pow(p1.getX() - p2.getX(),2) + Math.pow(p1.getY() - p2.getY(),2) + Math.pow(p1.getZ() - p2.getZ(),2));
	}
	
	
